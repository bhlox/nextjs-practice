import Head from "next/head";

import PlaceCard from "../components/PlaceCard";

import { collection, getDocs, limit, orderBy, query } from "firebase/firestore";
import { db } from "../firebase.config";

export default function Home({ posts }) {
  return (
    <>
      <Head>
        <title>TRAVEL VLOGS</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className="flex justify-center">
        <h2 className="text-3xl font-bold">Travel Vlogs</h2>
      </div>

      <div className="flex flex-wrap">
        {/* SINGLE CARD MAP HERE */}
        {posts.map((item) => (
          <PlaceCard key={item.id} {...item} />
        ))}
        {/* END SINGLE CARD */}
      </div>
    </>
  );
}

export async function getStaticProps(context) {
  const colRef = collection(db, "posts");
  try {
    const q = query(colRef, orderBy("timestamp", "desc"), limit(12));

    const snapshot = await getDocs(q);

    const posts = [];
    snapshot.docs.forEach((doc) => {
      posts.push({
        ...doc.data(),
        id: doc.id,
        timestamp: doc.data().timestamp.toDate().toDateString(),
      });
    });

    return {
      props: {
        posts,
      },
    };
  } catch (error) {
    return {
      props: {
        posts: [],
      },
    };
  }
}
